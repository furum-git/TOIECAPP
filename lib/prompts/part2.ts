export function buildPart2Prompt(count: number): string {
  return `TOEICのPart 2（応答問題）を${count}問作成してください。

【形式】
- 短い発話（疑問文・依頼・提案）に対して最も適切な応答を選ぶ
- 選択肢はA/B/Cの3択（Dは必ず空文字""）
- 誤答は「その場では言えない・質問に答えていない・論理的に噛み合わない」ものにする。「惜しいが間違い」ではなく「明確に不正解」であること
- 正解は「この質問にはこの返答しかない」と言えるレベルで適切なものにする
- WH疑問文・Yes/No疑問文・依頼提案文を均等に混ぜる

【JSONスキーマ】（マークダウンコードブロック不可、JSONのみ出力）
{
  "questions": [
    {
      "id": "p2_001",
      "part": 2,
      "questionText": "（最初の発話）",
      "choices": { "A": "...", "B": "...", "C": "...", "D": "" },
      "choicesJa": { "A": "（Aの日本語訳）", "B": "（Bの日本語訳）", "C": "（Cの日本語訳）", "D": "" },
      "correctAnswer": "A",
      "explanation": "正解の選択肢とその理由を一文で",
      "explanationPerChoice": {
        "A": "Aが正解なら「正解：〜の理由」、不正解なら「〜だから不正解」",
        "B": "Bが正解なら「正解：〜の理由」、不正解なら「〜だから不正解」",
        "C": "Cが正解なら「正解：〜の理由」、不正解なら「〜だから不正解」",
        "D": ""
      }
    }
  ]
}

${count}問分、idは p2_001, p2_002... と連番にしてください。`;
}
